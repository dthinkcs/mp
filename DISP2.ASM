CODE SEGMENT
	ASSUME CS:CODE
START:
	MOV BL, 0F3H;
	
	CALL DISP_BL;  DL, CL, AH CHANGES
	
	
DISP_BL:	
	MOV DL, BL
	MOV CL, 04H
	SHR DL, CL;
	CALL HEX_ASCII
	CALL DISP
	
		
	MOV DL, 00FH;
	AND DL, BL;
	CALL HEX_ASCII
	CALL DISP

	
	
	MOV AH, 4CH
	INT 21H
	RET

	
HEX_ASCII:
	CMP DL, 0AH
	JC ADD30
	ADD DL, 07H
	ADD30:	ADD DL, 30H
	RET
DISP:
	MOV AH, 02
	INT 21H
	RET

	
CODE ENDS
END START