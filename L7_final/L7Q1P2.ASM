DATA SEGMENT
    A DW 1234H,2222H,3333H,4444H,5555H,6666H,7777H,8888H,9999H,124AH
    B DW 124AH
    FOUND DB 'ELEMENT FOUND$'
    NFOUND DB 'ELEMENT NOT FOUND$'
DATA ENDS

CODE SEGMENT
    ASSUME CS:CODE, DS:DATA

DISP MACRO MSG
    LEA DX,MSG
    MOV AH,09H
    INT 21H
ENDM
START:
    MOV AX,DATA
    MOV DS,AX

    MOV BX,B

    MOV CX,0AH
    MOV SI,0

    BACK:  CMP BX,A[SI]
           JZ BREAK
           INC SI
           INC SI
           LOOP BACK

    DISP NFOUND
    JMP EXIT

    BREAK: DISP FOUND      

    EXIT: MOV AH,4CH
          INT 21H

    CODE ENDS
END START
