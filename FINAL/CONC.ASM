	ASSUME CS:CODE,DS:DATA
START:
	MOV AX,DATA
	MOV DS,AX
	MOV CX,9
	BACK1: PUSH CX
	LEA SI,ARRAY1
	BACK:
	MOV AL,[SI]
	CMP AL,[SI+1]
	JC DOWN
	XCHG AL,[SI+1]
	MOV [SI],AL
	DOWN:INC SI
	LOOP BACK
	POP CX
	LOOP BACK1
	MOV AH,4CH
	INT 21H
CODE ENDS
END START
