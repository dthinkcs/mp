DATA SEGMENT
	ARRAY1 DB 12H,23H,34H,45H,56H,67H,78H,89H,32H,50H
	NUM1 DB 99H
	MSG1 DB "FOUND$"
	MSG2 DB "NOTFOUND$"
DATA ENDS
CODE SEGMENT
	ASSUME CS:CODE,DS:DATA
START:
	MOV AX,DATA
	MOV DS,AX
	MOV CX,10
	LEA SI,ARRAY1
	BACK:MOV AL,[SI]
	CMP AL,NUM1
	JE FOUND
	INC SI
	LOOP BACK
	LEA DX,MSG2
	MOV AH,09
	INT 21H
	MOV AH,4CH
	INT 21H
	FOUND:LEA DX,MSG1
	MOV AH,09
	INT 21H
	MOV AH,4CH
	INT 21H
CODE ENDS
END START



