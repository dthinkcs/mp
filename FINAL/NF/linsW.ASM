DATA SEGMENT
	ARRAY1 DW 0012H,0023H,0034H,0045H,0056H,0067H,0078H,0089H,0032H,0050H
	NUM1 DW 0067H
	MSG1 DB "FOUND$"
	MSG2 DB "NOTFOUND$"
DATA ENDS
CODE SEGMENT
	ASSUME CS:CODE,DS:DATA
START:
	MOV AX,DATA
	MOV DS,AX
	MOV CX,10
	LEA SI,ARRAY1
	BACK:MOV AX,[SI]
	CMP AX,NUM1
	JE FOUND
	INC SI
	INC SI
	LOOP BACK
	LEA DX,MSG2
	MOV AH,09
	INT 21H
	MOV AH,4CH
	INT 21H
	FOUND:LEA DX,MSG1
	MOV AH,09
	INT 21H
	MOV AH,4CH
	INT 21H
CODE ENDS
END START



