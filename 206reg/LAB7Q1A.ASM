DATA SEGMENT
ARRAY DB 01H,02H,03H,04H,05H,06H,07H,08H,09H,10H
ELE DB 08H
FLAG DB ?
MSG1 DB "fOUND"
MSG2 DB "NOT FOUND"
DATA ENDS
PRINTMSG MACRO MSG
LEA DX,MSG
MOV AH,09
INT 21H
ENDM
CODE SEGMENT
ASSUME CS:CODE,DS:DATA
START: MOV AX,DATA
MOV DS,AX
LEA SI,ARRAY
MOV AL,ELE
MOV CX,10
UP:CMP AL,[SI]
JZ FOUND
INC SI
LOOP UP
NOT FOUND: PRINTSTR MSG1
JMP DOWN1
FOUND: MOV FLAG,01
PRINTSTR FOUND MSG
DOWN1:MOV AH,4CH
INT 21H
CODE ENDS
END START
