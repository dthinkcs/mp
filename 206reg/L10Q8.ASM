DATA SEGMENT
N1 DB 0AH
N2 DB 0F5H
DATA ENDS

CODE SEGMENT
ASSUME CS:CODE,DS:DATA
START:MOV AX,DATA
MOV DS,AX
MOV BH,N1 


UP:
MOV DL,13
MOV AH,02
INT 21H
MOV BL,BH
CALL DISPLAY
CALL DELAY

MOV AL,BH
ADD AL,01

CMP AL,N2
JE NEXT

MOV BH,AL
MOV DL,13
MOV AH,02
INT 21H


MOV AH,02
INT 21H
MOV DL,0FFH
MOV AH,06
INT 21H
JNZ DOWN
JMP UP
DOWN:MOV AH,4CH
INT 21H



NEXT:MOV DL,13
MOV AH,2
INT 21H
MOV BH,N2
UP1:MOV BL,BH
CALL DISPLAY
CALL DELAY

MOV AL,BH
SUB AL,01


CMP AL,N1
JE UP

MOV BH,AL
MOV DL,13
MOV AH,2
INT 21H

MOV AH,02
INT 21H
MOV DL,0FFH
MOV AH,06
INT 21H
JNZ DOWN2
JMP UP1
DOWN2:MOV AH,4CH
INT 21H






DELAY PROC
MOV SI,596
UP5:MOV DI,0FFH
 UP2: DEC DI
 JNZ UP2
 DEC SI
 JNZ UP5
 RET
 DELAY ENDP


DISPLAY PROC
MOV DL,BL
SHR DL,1
SHR DL,1
SHR DL,1
SHR DL,1

CMP DL,0AH
JC DOWN5
ADD DL,07H
DOWN5: ADD DL,30H
MOV AH,02
INT 21H

MOV DL,BL
AND DL,0FH
CMP DL,0AH
JC DOWN4
ADD DL,07H
DOWN4: ADD DL,30H

MOV AH,02
INT 21H
RET
DISPLAY ENDP

CODE ENDS
END START
