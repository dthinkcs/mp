DATA SEGMENT
	MSG DB 'ENTER PASSWORD : '
	PASSWORD DB 'PASSWRD$'
	ACT_PASS DB 7 DUP (?)
	VALID DB 'INCORRECT PASSWORD$'
	INVALID DB 'CORRECT PASSWORD$'
	MSG1 DB 10,13,'$'
DATA ENDS

PRINTSTR MACRO MSG
	LEA DX,MSG
	MOV AH,09H
	INT 21H
ENDM

CODE SEGMENT
	ASSUME CS:CODE,DS:DATA
	START:
		MOV AX,DATA
		MOV DS,AX
		MOV ES,AX
		PRINTSTR MSG
		LEA DI,ACT_PASS
		MOV CX,07
		CLD
	BACK:MOV AH,08
		INT 21H
		STOSB
		MOV DL,'*'
		MOV AH,02
		INT 21H
		LOOP BACK
		LEA SI,PASSWORD
		LEA DI,ACT_PASS
		MOV CX,07
		PRINTSTR MSG1
		REPE CMPSB
		JZ DOWN1
		PRINTSTR INVALID
		JMP EXIT1
	DOWN1: PRINTSTR VALID
	EXIT1: MOV AH,4CH
		INT 21H
CODE ENDS
END START

