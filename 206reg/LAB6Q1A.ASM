GCDMAC MACRO NUM1,NUM2
MOV AX,NUM1
MOV BX,NUM2
BACK: CMP AX,BX
JE EXIT
JC BIGBX
UP: MOV DX,0
DIV BX
CMP DX,0
JE EXIT
MOV AX,BX
MOV BX,DX
JMP BACK
BIGBX: XCHG AX,BX
JMP UP
EXIT: MOV GCD,BX
ENDM
