DATA SEGMENT
NUMBER DW 40,90
GCD DW ?
DATA ENDS
CODE SEGMENT
ASSUME CS:CODE,DS:DATA
START:
MOV AX,DATA
MOV DS,AX
MOV AX,NUMBER
MOV BX,NUMBER+2
CALL GCDPROC
MOV AH,4CH
INT 21H
GCDPROC PROC
BACK:
CMP AX,BX
JE EXIT
JC BIGBX
UP: MOV DX,0
DIV BX
CMP DX,0
JE EXIT
MOV AX,DX
BIGBX:
XCHG AX,BX
JMP UP
EXIT:
MOV GCD,BX
RET
GCDPROC ENDP
CODE ENDS
END START
