PRINT MACRO MSG
	LEA DX,MSG
	MOV AH,09
	INT 21H
ENDM

DATA SEGMENT
	MSG1 DB 10,13,'$'
	MSG DB 'ENTER STRING$'
	BUFFER DB 20
	LEN DB ?
	STR DB 20 DUP(?)
	PAL DB 'PALINDROME$'
	NPAL DB 'NOT PALINDROME$'
	DATA ENDS
CODE SEGMENT

	ASSUME CS:CODE,DS:DATA,ES:DATA
	START: MOV AX,DATA
		MOV DS,AX
		MOV ES,AX
		PRINT MSG
		PRINT MSG1
		LEA DX,BUFFER
		MOV AH,0AH
		INT 21H
		MOV CL,LEN
		MOV CH,00
		LEA SI,STR
		MOV DI,SI
		ADD DI,CX
		DEC DI
		SHR CX,01
	BACK:
		CLD
		LODSB
		STD
		SCASB
		JNZ DOWN
		LOOP BACK
		PRINT PAL
		JMP EXIT1
	DOWN:PRINT NPAL
	EXIT1: MOV AH,4CH
		INT 21H
CODE ENDS
END START
