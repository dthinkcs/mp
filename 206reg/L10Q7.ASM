DATA SEGMENT
N1 DB 06H
N2 DB 0AFH
DATA ENDS
CODE SEGMENT
ASSUME CS:CODE,DS:DATA
START:MOV AX,DATA
MOV DS,AX
START1: MOV BH,N1

UP:MOV BL,BH
MOV DL,13
MOV AH,2
INT 21H
CALL DISPLAY
CALL DELAY

MOV AL,BH
ADD AL,01
CMP AL,N2
JA START1
MOV BH,AL
MOV DL,13
MOV AH,02
INT 21H
MOV DL,0FFH
MOV AH,06
INT 21H
JNZ DOWN
JMP UP
DOWN:MOV AH,4CH
INT 21H


DELAY PROC
MOV SI,596
UP1:MOV DI,0FFH
 UP2: DEC DI
 JNZ UP2
 DEC SI
 JNZ UP1
 RET
 DELAY ENDP


DISPLAY PROC
MOV DL,BL
SHR DL,1
SHR DL,1
SHR DL,1
SHR DL,1

CMP DL,0AH
JC DOWN1
ADD DL,07H
DOWN1: ADD DL,30H
MOV AH,02
INT 21H

MOV DL,BL
AND DL,0FH
CMP DL,0AH
JC DOWN2
ADD DL,07H
DOWN2: ADD DL,30H

MOV AH,02
INT 21H
RET
DISPLAY ENDP

CODE ENDS
END START
