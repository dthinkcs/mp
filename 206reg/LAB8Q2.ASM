DATA SEGMENT
	MSG1 DB 'ENTER HEX NUMBER $'
	NEXT1 DB 10,13,'$'
	MSG2 DB 'SUM IS $'
DATA ENDS
PRINT MACRO MSG
	LEA DX,MSG
	MOV AH,09
	INT 21H
ENDM
CODE SEGMENT
	ASSUME CS:CODE,DS:DATA
	START: MOV AX,DATA
		MOV DS,AX
		MOV AX,0

		PRINT MSG1
		CALL GET_2DIG_HEX
		MOV BL,AL
		MOV CL,04
		ROR BL,CL
		CALL GET_2DIG_HEX
		ADD BL,AL
		PRINT NEXT1
		PRINT MSG1
		CALL GET_2DIG_HEX
		MOV DL,AL
		MOV CL,04
		ROR DL,CL
		CALL GET_2DIG_HEX
		ADD DL,AL
		ADD BL,DL
		MOV CL,BL
		PRINT NEXT1
		PRINT MSG2
		MOV AX,0
		ADC AL,0
		CALL DISP
		MOV AH,4CH
		INT 21H

	GET_2DIG_HEX: MOV AH,01
		INT 21H
		CMP AL,39H
		JBE SKIP
		SUB AL,07H
	SKIP:SUB AL,30H
		RET

	DISP: MOV BL,10H
		DIV BL
		MOV BL,AH
		MOV DL,AL
		CMP DL,09H
		JBE SKIP1
		ADD DL,07H
	SKIP1:
		ADD DL,30H
		MOV AH,02
		INT 21H
		MOV DL,BL CMP DL,09H
		JBE SKIP2
		ADD DL,07H
	SKIP2: ADD DL,30H
		MOV AH,02
		INT 21H
		RET
		
CODE ENDS
END START

