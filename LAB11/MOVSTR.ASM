DATA SEGMENT
	A DB 'ARUNIMA$'
	ROW DB 12
	COL DB 20
DATA ENDS

CODE SEGMENT
ASSUME CS:CODE, DS:DATA
START:
	MOV AX, DATA
	MOV DS, AX

	MOV BL, ROW
	MOV BH, COL
BACK2:MOV AL, 160
	MUL BL
	MOV SI, AX
	SHL BH, 1
	MOV BL, 0
	XCHG BL, BH
	ADD SI, BX
		
	BACK1:
		MOV AH, 0
		MOV AL, 03
		INT 10H
	  CALL DISP
	back:
		MOV DL, 0FFH
		MOV AH, 06
		INT 21H
		jz back
		CMP AL, 'U'
		JZ UP
		CMP AL, 'D'
		JZ DOWN
		CMP AL, 'L'
		JZ LEFT
		CMP AL, 'R'
		JZ RIGHT
		CMP AL, 'E'
		JZ EXIT
		
			
	UP: MOV BL, ROW
		MOV BH, COL
		DEC BL
		MOV ROW, BL
		JMP BACK2
		
	DOWN: MOV BL, ROW
		MOV BH, COL
		INC BL
		MOV ROW, BL
		JMP BACK2
	
	LEFT:MOV BL, ROW
		MOV BH, COL
		DEC BH
		MOV COL, BH
		JMP BACK2
	
	RIGHT:MOV BL, ROW
		MOV BH, COL
		INC BH
		MOV COL, BH
		JMP BACK2

	EXIT: MOV AH, 4CH
		INT 21H
	
	DISP:
		LEA DI, A
		
		MOV DL, 7
	
	BK: MOV CL, [DI]
		PUSH DS
		MOV AX, 0B800H
		MOV DS, AX
		MOV BYTE PTR [SI], CL
		MOV BYTE PTR [SI+1], 00010110B
		INC SI
		INC SI
		INC DI
		POP DS
		DEC DL
		JNZ BK
		
	RET
CODE ENDS
END START
