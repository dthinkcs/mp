		MOV AH, 01
		INT 21H
		
		LEA DX, NEXTLINE
		MOV AH, 09H
        INT 21H
		
		LEA DX, OUTMSG
		MOV AH, 09H
        INT 21H
		
		MOV DL, AL
		MOV AH, 02H
        INT 21H
		
		MOV AH, 4CH
		INT 21H
		
CODE ENDS
END START



