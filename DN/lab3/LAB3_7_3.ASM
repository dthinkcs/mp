DATA SEGMENT
	A DD ?
	B DD ?
	SUM DD ?
	CARRY DB 00H
DATA ENDS

CODE SEGMENT
ASSUME CS:CODE, DS:DATA
START:
	MOV DX, DATA
	MOV DS, DX
    	MOV CX, 4
	MOV SI, 0
	CLC
	
	BACK: 
		MOV AL, BYTE PTR A[SI]
		ADC AL, BYTE PTR B[SI]
		DAA
        	MOV BYTE PTR SUM[SI], AL
		INC SI
		LOOP BACK
		
	ADC CARRY, 0
	
	MOV AH, 4CH
	INT 21H

CODE ENDS
END START
